////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
function min(a, b) {
  if (a < b) {
    return a;
  } else {
    return b;
  }
}
////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
function isEven(number) {
  if (number < 0) {
    return false; 
  } else if (number === 0) {
    return true; 
  } else if (number === 1) {
    return false; 
  } else {
    return isEven(number - 2); 
  }
}
////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
function countChars(string, character) {
  let count = 0;
  for (let i = 0; i < string.length; i++) {
    if (string[i] === character) {
      count++;
    }
  }
  return count;
}
////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
function countBs(string) {
  return countChars(string, 'B');
}
